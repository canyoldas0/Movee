import UIKit

extension UIColor {
    
    class var moveeBlack: UIColor {
        return UIColor(red: 0.03, green: 0.04, blue: 0.04, alpha: 1.00)
    }
    
    class var moveeBlue: UIColor {
        return UIColor(red: 0.00, green: 0.24, blue: 1.00, alpha: 1.00)
    }
    
    class var moveeWhite: UIColor {
        return .white
    }
}
